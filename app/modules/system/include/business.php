function business_format_tel($value) {

	// string to char array
	$value_array = preg_split('//', $value, -1, PREG_SPLIT_NO_EMPTY);
	$value = '';

	foreach($value_array as $char) {
		if (is_numeric($char) || $char == '+' || $char == '(' || $char == ')') {
			$value .= $char;
		}
	}

	return($value);
}

