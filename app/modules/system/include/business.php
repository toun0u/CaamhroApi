function business_get_avancement($date_debut, $date_fin) {
	$datejour = date(dims_const::DIMS_DATEFORMAT_US);
	if ($date_fin == '' || $date_debut == '') $avancement = 0;
	elseif ($datejour >= $date_fin) $avancement = 100;
	else {
		$duree_dossier = business_datediff($date_debut,$date_fin);
		if ($duree_dossier == 0) $avancement = 100;
		else {
			$temps_ecoule = business_datediff($date_debut,$datejour);
			$avancement = sprintf("%d",($temps_ecoule * 100) / $duree_dossier);
			if ($avancement < 0) $avancement = 0;
		}
	}

	return($avancement);
}

